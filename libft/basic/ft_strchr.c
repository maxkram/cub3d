#include "libft_basic.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	while (*p && *p != (char)c)
		p++;
	if ((char)c != '\0' && *p == '\0')
		return (0);
	return (p);
}
